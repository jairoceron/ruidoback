package mintrabajo.dashboard.model;


public class Grupo {
    private Integer despacho;
    private Integer ivc;
    private Integer rcc;
    
    public Grupo() {}

	public Integer getDespacho() {
		return despacho;
	}

	public void setDespacho(Integer despacho) {
		this.despacho = despacho;
	}

	public Integer getIvc() {
		return ivc;
	}

	public void setIvc(Integer ivc) {
		this.ivc = ivc;
	}

	public Integer getRcc() {
		return rcc;
	}

	public void setRcc(Integer rcc) {
		this.rcc = rcc;
	}
    
    
    
}