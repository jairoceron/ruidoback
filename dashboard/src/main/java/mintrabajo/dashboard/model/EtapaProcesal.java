package mintrabajo.dashboard.model;


public class EtapaProcesal {
    private Integer idEtapaPro;
    private String nombreEtapa;
    public EtapaProcesal() {}
	public Integer getIdEtapaPro() {
		return idEtapaPro;
	}
	public void setIdEtapaPro(Integer idEtapaPro) {
		this.idEtapaPro = idEtapaPro;
	}
	public String getNombreEtapa() {
		return nombreEtapa;
	}
	public void setNombreEtapa(String nombreEtapa) {
		this.nombreEtapa = nombreEtapa;
	}
    
}

