package mintrabajo.dashboard.model;


public class Expvencer {
    Integer unasemana;
    Integer unmes;
    Integer tresmeses;
    public Expvencer() {}
	public Integer getUnasemana() {
		return unasemana;
	}
	public void setUnasemana(Integer unasemana) {
		this.unasemana = unasemana;
	}
	public Integer getUnmes() {
		return unmes;
	}
	public void setUnmes(Integer unmes) {
		this.unmes = unmes;
	}
	public Integer getTresmeses() {
		return tresmeses;
	}
	public void setTresmeses(Integer tresmeses) {
		this.tresmeses = tresmeses;
	}
    
}